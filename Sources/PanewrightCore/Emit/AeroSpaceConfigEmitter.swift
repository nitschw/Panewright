/// Renders a ``PanewrightConfig`` as an `aerospace.toml` that Panewright owns
/// and AeroSpace consumes.
public enum AeroSpaceConfigEmitter {
    public static func emit(_ config: PanewrightConfig) -> String {
        var lines: [String] = []
        lines.append("# Generated by Panewright — do not edit by hand.")
        lines.append("# Edit panewright.toml (or use the Panewright app) instead.")
        lines.append("config-version = 2")
        lines.append("start-at-login = false")
        if config.statusBar.enabled {
            lines.append(
                "exec-on-workspace-change = ['/bin/bash', '-c', '/opt/homebrew/bin/sketchybar --trigger aerospace_workspace_change FOCUSED_WORKSPACE=$AEROSPACE_FOCUSED_WORKSPACE']"
            )
        }
        lines.append("")
        // i3 split behavior: workspaces come up tiling, nested splits alternate
        // orientation, and redundant single-child containers are flattened.
        lines.append("default-root-container-layout = 'tiles'")
        lines.append("enable-normalization-flatten-containers = true")
        lines.append("enable-normalization-opposite-orientation-for-nested-containers = true")
        lines.append("")
        // i3 mental model: numbered workspaces always exist, even when empty.
        let workspaces = workspaceNumbers(in: config.bindings)
        if !workspaces.isEmpty {
            let list = workspaces.map { "'\($0)'" }.joined(separator: ", ")
            lines.append("persistent-workspaces = [\(list)]")
            lines.append("")
        }
        if !config.workspaceMonitors.isEmpty {
            lines.append("[workspace-to-monitor-force-assignment]")
            for (workspace, monitor) in config.workspaceMonitors.sorted(by: { $0.key < $1.key }) {
                lines.append("\(workspace) = '\(monitor)'")
            }
            lines.append("")
        }
        for appId in config.floatingApps {
            lines.append("[[on-window-detected]]")
            lines.append("if.app-id = '\(appId)'")
            lines.append("run = 'layout floating'")
            lines.append("")
        }
        lines.append("[gaps]")
        lines.append("inner.horizontal = \(config.gaps.inner)")
        lines.append("inner.vertical = \(config.gaps.inner)")
        // The status bar, when present, becomes the top edge of the desktop:
        // tiles reserve its strip; without it the normal edge applies.
        let topGap =
            config.gaps.outer
            + (config.statusBar.enabled
                ? SketchyBarConfigEmitter.reservedTopGap(for: config.statusBar.theme) : 0)
        lines.append("outer.left = \(config.gaps.outer)")
        lines.append("outer.bottom = \(config.gaps.outer)")
        lines.append("outer.top = \(topGap)")
        lines.append("outer.right = \(config.gaps.outer)")
        lines.append("")
        let barEnabled = config.statusBar.enabled
        lines.append("[mode.main.binding]")
        if config.modifier == .leader {
            // tmux-style: the prefix is the only global binding; commands are
            // bare keys in a one-shot mode that falls back to main.
            let enterPanewright =
                barEnabled
                ? "['mode panewright', '\(modeTrigger("panewright"))']"
                : "'mode panewright'"
            lines.append("\(config.leaderKey) = \(enterPanewright)")
            lines.append("")
            lines.append("[mode.panewright.binding]")
            let escape =
                barEnabled
                ? "['mode main', '\(modeTrigger("main"))']" : "'mode main'"
            lines.append("esc = \(escape)")
            for binding in config.bindings {
                lines.append(
                    "\(binding.key) = \(leaderBindingValue(binding, statusBarEnabled: barEnabled))"
                )
            }
        } else {
            for binding in config.bindings {
                let combo = keyCombo(modifier: config.modifier, key: binding.key)
                lines.append("\(combo) = \(bindingValue(binding, statusBarEnabled: barEnabled))")
            }
        }
        // Mode bindings are bare keys — that's the point of a mode.
        for mode in config.modes {
            lines.append("")
            lines.append("[mode.\(mode.name).binding]")
            for binding in mode.bindings {
                lines.append("\(binding.key) = \(bindingValue(binding, statusBarEnabled: barEnabled))")
            }
        }
        return lines.joined(separator: "\n") + "\n"
    }

    /// AeroSpace key syntax: modifiers joined by `-`, key last (`alt-shift-1`).
    ///
    /// The hyper base is Cmd+Opt+Ctrl *without* Shift so that i3-style
    /// `$mod+Shift+…` chords remain distinguishable from plain `$mod+…`.
    static func keyCombo(modifier: PanewrightConfig.Modifier, key: String) -> String {
        // Leader style never chords: bindings live bare inside the
        // panewright mode, so a combo request passes the key through.
        if modifier == .leader {
            return key
        }
        let base =
            switch modifier {
            case .hyper: "cmd-alt-ctrl"
            case .alt: "alt"
            case .cmd: "cmd"
            case .ctrlAlt: "ctrl-alt"
            case .ctrlCmd: "ctrl-cmd"
            case .leader: ""
            }
        if key.hasPrefix("shift-") {
            return "\(base)-shift-\(key.dropFirst("shift-".count))"
        }
        return "\(base)-\(key)"
    }

    static func workspaceNumbers(in bindings: [PanewrightConfig.Binding]) -> [Int] {
        var numbers: Set<Int> = []
        for action in bindings.flatMap(\.actions) {
            switch action {
            case .workspace(let n), .moveToWorkspace(let n):
                numbers.insert(n)
            default:
                break
            }
        }
        return numbers.sorted()
    }

    /// The status bar can't observe mode changes, so every mode switch pings
    /// it explicitly as part of the binding's command chain.
    static func modeTrigger(_ name: String) -> String {
        "exec-and-forget /opt/homebrew/bin/sketchybar --trigger panewright_mode MODE=\(name)"
    }

    /// A binding's TOML value: a single command as a string, a chain as an array.
    static func bindingValue(
        _ binding: PanewrightConfig.Binding, statusBarEnabled: Bool = false
    ) -> String {
        var commands: [String] = []
        for action in binding.actions {
            commands.append("'\(command(for: action))'")
            if statusBarEnabled, case .enterMode(let name) = action {
                commands.append("'\(modeTrigger(name))'")
            }
        }
        return commands.count == 1 ? commands[0] : "[\(commands.joined(separator: ", "))]"
    }

    /// Leader-mode value: one-shot — every chain falls back to main unless it
    /// already ends by entering another mode.
    static func leaderBindingValue(
        _ binding: PanewrightConfig.Binding, statusBarEnabled: Bool = false
    ) -> String {
        var actions = binding.actions
        let endsInModeChange: Bool
        if let last = actions.last, case .enterMode = last {
            endsInModeChange = true
        } else {
            endsInModeChange = false
        }
        if !endsInModeChange {
            actions.append(.enterMode("main"))
        }
        return bindingValue(
            PanewrightConfig.Binding(key: binding.key, actions: actions),
            statusBarEnabled: statusBarEnabled)
    }

    static func command(for action: PanewrightConfig.Action) -> String {
        switch action {
        case .workspace(let n): "workspace \(n)"
        case .moveToWorkspace(let n): "move-node-to-workspace \(n)"
        case .focus(let direction): "focus \(direction.rawValue)"
        case .move(let direction): "move \(direction.rawValue)"
        case .layoutTiles: "layout tiles horizontal vertical"
        case .layoutAccordion: "layout accordion horizontal vertical"
        case .fullscreen: "fullscreen"
        case .toggleFloating: "layout floating tiling"
        case .focusMonitor(let target): "focus-monitor \(target.rawValue)"
        case .moveToMonitor(let target): "move-node-to-monitor \(target.rawValue)"
        case .resize(let dimension, let delta):
            "resize \(dimension.rawValue) \(delta >= 0 ? "+\(delta)" : "\(delta)")"
        case .joinWith(let direction): "join-with \(direction.rawValue)"
        case .flattenWorkspace: "flatten-workspace-tree"
        case .enterMode(let name): "mode \(name)"
        case .exec(let command): "exec-and-forget \(command)"
        case .close: "close"
        }
    }
}
